import { nextTestSetup } from 'e2e-utils'

describe('typed-routes', () => {
  const { next } = nextTestSetup({
    files: __dirname,
  })

  // Test for route types file generation
  it('should generate route types file correctly', async () => {
    // Read the generated route types file
    const routeTypesContent = await next.readFile('.next/types/routes.ts')

    // Check that the file contains expected app route types (including route groups)
    expect(routeTypesContent).toContain(
      'type AppRoutes = "/" | "/blog/[slug]" | "/dashboard" | "/dashboard/settings" | "/docs/[...slug]" | "/gallery" | "/gallery/photo/[id]" | "/gallery/test" | "/login" | "/reset-password/[token]" | "/shop/[[...category]]"'
    )

    // Check that the file contains expected page route types
    expect(routeTypesContent).toContain(
      'type PageRoutes = "/about" | "/users/[id]"'
    )

    // Check that the file contains expected layout route types
    expect(routeTypesContent).toContain(
      'type LayoutRoutes = "/" | "/dashboard"'
    )

    // Check for combined Routes type
    expect(routeTypesContent).toContain(
      'type Routes = AppRoutes | PageRoutes | LayoutRoutes'
    )

    // Check for ParamMap type with all route parameters (including route group routes)
    expect(routeTypesContent).toContain('type ParamMap = {')
    expect(routeTypesContent).toContain("'/': { }")
    expect(routeTypesContent).toContain("'/about': { }")
    expect(routeTypesContent).toContain("'/blog/[slug]': { slug: string; }")
    expect(routeTypesContent).toContain("'/dashboard': { }")
    expect(routeTypesContent).toContain("'/dashboard/settings': { }")
    expect(routeTypesContent).toContain(
      "'/docs/[...slug]': { slug: string[]; }"
    )
    expect(routeTypesContent).toContain("'/gallery': { }")
    expect(routeTypesContent).toContain(
      "'/gallery/photo/[id]': { id: string; }"
    )
    expect(routeTypesContent).toContain("'/gallery/test': { }")
    expect(routeTypesContent).toContain("'/login': { }")
    expect(routeTypesContent).toContain(
      "'/reset-password/[token]': { token: string; }"
    )
    expect(routeTypesContent).toContain(
      "'/shop/[[...category]]': { category?: string[]; }"
    )
    expect(routeTypesContent).toContain("'/users/[id]': { id: string; }")

    // Check for LayoutSlotMap type with parallel routes
    expect(routeTypesContent).toContain('type LayoutSlotMap = {')
    expect(routeTypesContent).toContain("'/': never")
    expect(routeTypesContent).toContain('\'/dashboard\': "analytics" | "team"')

    // Check for ParamsOf helper type
    expect(routeTypesContent).toContain(
      'export type ParamsOf<P extends Routes> = ParamMap[P]'
    )

    // Check for global PageProps and LayoutProps types
    expect(routeTypesContent).toContain('type PageProps<P extends AppRoutes>')
    expect(routeTypesContent).toContain(
      'type LayoutProps<P extends LayoutRoutes>'
    )

    // Check for exported types
    expect(routeTypesContent).toContain(
      'export type { AppRoutes, PageRoutes, LayoutRoutes }'
    )

    // Check for auto-generated header comment
    expect(routeTypesContent).toContain(
      '// This file is generated automatically by Next.js'
    )
    expect(routeTypesContent).toContain('// Do not edit this file manually')
  })

  // Test that TypeScript compilation works with the generated types
  it('should compile TypeScript files using the generated types', async () => {
    // Create a TypeScript file that uses the generated types
    await next.patchFile(
      'lib/type-test.ts',
      `
      import type { AppRoutes, PageRoutes, LayoutRoutes } from 'next'
      
      // Test that the types are available and can be used
      type TestAppRoute = AppRoutes
      type TestPageRoute = PageRoutes
      type TestLayoutRoute = LayoutRoutes
      
      // Test that ParamsOf works correctly
      type BlogParams = Parameters<(props: PageProps<"/blog/[slug]">) => void>[0]['params']
      type DocsParams = Parameters<(props: PageProps<"/docs/[...slug]">) => void>[0]['params']
      type ShopParams = Parameters<(props: PageProps<"/shop/[[...category]]">) => void>[0]['params']
      type GalleryPhotoParams = Parameters<(props: PageProps<"/gallery/photo/[id]">) => void>[0]['params']
      type ResetPasswordParams = Parameters<(props: PageProps<"/reset-password/[token]">) => void>[0]['params']
      
      // Test that LayoutProps works correctly
      type RootLayoutProps = Parameters<(props: LayoutProps<"/">) => void>[0]
      type DashboardLayoutProps = Parameters<(props: LayoutProps<"/dashboard">) => void>[0]
      
      export const typeTest = {
        appRoutes: [] as TestAppRoute[],
        pageRoutes: [] as TestPageRoute[],
        layoutRoutes: [] as TestLayoutRoute[],
      }
    `
    )

    // Try to build the project to check TypeScript compilation
    try {
      await next.fetch('/') // This should trigger compilation

      // If we get here without errors, TypeScript compilation worked
      expect(true).toBe(true)
    } catch (error) {
      // If there's a compilation error, the test should fail
      throw new Error(`TypeScript compilation failed: ${error}`)
    }

    // Clean up
    await next.deleteFile('lib/type-test.ts')
  })

  // Test route groups functionality
  it('should work with route groups', async () => {
    const $ = await next.render$('/login')
    expect($('h1').text()).toBe('Login')
    expect($('h2').text()).toBe('Authentication') // From the auth layout
    expect(
      $('div:contains("Please sign in to your account")').length
    ).toBeGreaterThan(0)
  })

  // Test route groups with dynamic routes
  it('should work with route groups and dynamic routes', async () => {
    const $ = await next.render$('/reset-password/abc123')
    expect($('h1').text()).toBe('Reset Password')
    expect($('h2').text()).toBe('Authentication') // From the auth layout
    expect(
      $('div:contains("Enter your new password using token: abc123")').length
    ).toBeGreaterThan(0)
  })

  // Test parallel routes functionality
  it('should work with parallel routes', async () => {
    const $ = await next.render$('/dashboard')

    // Check that the main layout structure is present
    expect($('h2').text()).toBe('Dashboard Layout')
    expect($('h3').length).toBe(3) // Main Content, Analytics, Team

    // Check that parallel route content is rendered
    expect($('div:contains("Analytics Dashboard")').length).toBeGreaterThan(0)
    expect($('div:contains("Team Dashboard")').length).toBeGreaterThan(0)
    expect($('div:contains("Dashboard Home")').length).toBeGreaterThan(0)
  })

  // Test parallel routes with nested pages
  it('should work with parallel routes nested pages', async () => {
    const $ = await next.render$('/dashboard/settings')

    // Check that the main layout structure is present
    expect($('h2').text()).toBe('Dashboard Layout')

    // Check that parallel route settings content is rendered
    expect($('div:contains("Analytics Settings")').length).toBeGreaterThan(0)
    expect($('div:contains("Team Settings")').length).toBeGreaterThan(0)
    expect($('div:contains("Dashboard Settings")').length).toBeGreaterThan(0)
  })

  // Test that intercepting routes structure is correctly detected
  it('should detect intercepting routes in types', async () => {
    const routeTypesContent = await next.readFile('.next/types/routes.ts')

    // The intercepting routes are detected as regular routes in the type system
    // They share the same route parameters as the routes they intercept
    expect(routeTypesContent).toContain(
      "'/gallery/photo/[id]': { id: string; }"
    )
    expect(routeTypesContent).toContain("'/gallery/test': { }")

    // Verify the intercepting route file exists
    const interceptingRoute = await next.readFile(
      'app/gallery/test/(..)photo/[id]/page.tsx'
    )
    expect(interceptingRoute).toContain('InterceptedPhotoPage')
    expect(interceptingRoute).toContain('Intercepted Photo')
  })

  // Test gallery routes
  it('should work with gallery routes', async () => {
    const $ = await next.render$('/gallery')
    expect($('h1').text()).toBe('Gallery')
    expect(
      $('div:contains("Browse our photo collection")').length
    ).toBeGreaterThan(0)

    const $test = await next.render$('/gallery/test')
    expect($test('h1').text()).toBe('Test Page')
    expect(
      $test('div:contains("This page demonstrates intercepting routes")').length
    ).toBeGreaterThan(0)
  })

  // Test photo routes
  it('should work with photo routes', async () => {
    const $ = await next.render$('/gallery/photo/123')
    expect($('h2').text()).toBe('Photo 123')
    expect(
      $('div:contains("Viewing photo with ID: 123")').length
    ).toBeGreaterThan(0)
  })

  // Test that route types file is updated when routes change
  it('should update route types file when routes change', async () => {
    // Create a new route file
    await next.patchFile(
      'app/new-route/page.tsx',
      `
      export default function NewRoute() {
        return <div>New Route</div>
      }
    `
    )

    // Wait for the file to be processed
    await next.fetch('/new-route')

    // Read the updated route types file
    const routeTypesContent = await next.readFile('.next/types/routes.ts')

    // Check that the new route is included
    expect(routeTypesContent).toContain('"/new-route"')
    expect(routeTypesContent).toContain("'/new-route': { }")

    // Clean up
    await next.deleteFile('app/new-route/page.tsx')
  })

  // Test basic functionality still works
  it('should work using cheerio', async () => {
    const $ = await next.render$('/')
    expect($('p').text()).toBe('hello world')
  })

  it('should work with dynamic routes', async () => {
    const $ = await next.render$('/blog/test-slug')
    expect($('div').text()).toBe('Blog post: test-slug')
  })

  it('should work with catch-all routes', async () => {
    const $ = await next.render$('/docs/guide/getting-started')
    expect($('div').text()).toBe('Docs: guide/getting-started')
  })

  it('should work with optional catch-all routes', async () => {
    const $ = await next.render$('/shop')
    expect($('div').text()).toBe('Shop: all')

    const $category = await next.render$('/shop/electronics/phones')
    expect($category('div').text()).toBe('Shop: electronics/phones')
  })

  it('should work with dashboard layout', async () => {
    const $ = await next.render$('/dashboard')
    expect($('h2').text()).toBe('Dashboard Layout')
    expect($('div:contains("Dashboard Home")').length).toBeGreaterThan(0)
  })

  it('should work with pages router routes', async () => {
    const html = await next.render('/about')
    expect(html).toContain('About Page')

    const userHtml = await next.render('/users/123')
    expect(userHtml).toContain('User:')
    expect(userHtml).toContain('123')
  })
})
